<h1 style="color: #ed1c24;">Каталог лепнины: чтобы ничего не пропустить</h1>
<p align="justify">
	 Бесплатный каталог лепнины необходим для того, чтобы вы могли удобно и быстро найти все необходимые декоративные элементы в многообразии продукции DECOMASTER.
</p>
<p align="justify">
	 Каталоги DECOMASTER позволяют не просто отыскать необходимый декор, но и найти удачные варианты сочетания различных украшений.
</p>
<p align="justify">
</p>
<h2 style="color: #ed1c24;">Что вы найдете в наших каталогах</h2>
<p align="justify">
	 Наши каталоги лепнины очень подробны, ведь задача их составителей – сделать поиск декора как можно более быстрым. Кроме того, мы постарались сделать так, чтобы вы могли получить максимум информации о любом изделии. В каждом каталоге вы найдете:
</p>
<ul>
	<li align="justify">Названия и фотографии каждого плинтуса, молдинга, профиля и других декоративных элементов.</li>
	<li align="justify">Все элементы, подходящие друг другу рисунку, дизайну и размеру, собраны вместе, так что не нужно искать их по всем страницам.</li>
	<li align="justify">Рядом с изображением каждого изделия в каталоге лепнины вы увидите цифры – его артикул. Введите их в строку поиска на сайте, и он сопроводит вас на страницу товара.</li>
	<li align="justify">В буклетах содержится вся информация о материалах, из которых выполнены декоративные элементы.</li>
	<li align="justify">Монтаж изделий требует определенного навыка. Именно поэтому в каталогах лепнины есть информация о монтаже, а также инструментах, которые могут понадобиться.</li>
	<li align="justify">Страницы, посвященные монтажным размерам, позволяю подобрать изделия без ошибки.</li>
	<li align="justify">Специальный каталог, посвященный стилям в интерьере, подробно описывает, какие декоративные элементы присущи тому или иному дизайну.</li>
</ul>
<p align="justify">
	 Все, что нужно знать об изделиях DECOMASTER, подробно изложено в каталогах. Информация систематизирована таким образом, что вы можете совершить поиск как конкретных предметов, так и декора определенного стиля, дизайна и размера.
</p>
 Как получить нужный каталог?
<p align="justify">
	 Все каталоги лепнины DECOMASTER доступны для свободного скачивания:
</p>
<ul>
	<li align="justify">Скачайте каталог в формате pdf и просматривать его на планшете, компьютере или другом устройстве.</li>
	<li align="justify">Файлы полностью готовы к печати – вы можете распечатать собственный бумажный каталог.</li>
	<li align="justify">Также можно воспользоваться каталогом даже без скачивания – просто откройте его в окне браузера.</li>
</ul>
